"""
Tipo de dado bool (booleano)

Ao questionar algo em um programa, só existem duas respostas:
sim (True) ou não (False).

Existem vários operadores para "questionar".
Dente eles o ==, que é um operador lógico que questiona se 
um valor é igual a outro
"""

print(10 == 10)     # sim => True (verdadeiro)
print(10 == "10")   # não => False (falso)

print(type(10 == 10))
print(type(10 == 11))
print(type(True))
print(type(False))

