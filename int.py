"""
Tipos int e float

int -> Número inteiro
O tipo int representa qualquer número positivo ou 
negativo. int sem sinal é considerado positivo

float -> Número com ponto flutuante
O tipo float representa qualquer número positivo ou
negativo com ponto flutuante. float sem sinal é 
considerado positivo.

A função type mostra o tipo que o Python inferiu ao
valor.
"""

print(11)   # int
print(-11)  # int
print(0)    # int

print(1.1)  # float
print(1.1)  # float

print(  type('marcin')  )
print(  type(1)         )
print(  type(1.1)       )
print(  type(-1.1)      )
print(  type(0)         )

