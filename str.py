"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234) # Não é uma String
print('1234') # É uma string

# Aspas simples
print('Dj Marcin')

# Aspas duplas
print("Dj Marcin")

# Escape*
print('Dj \'Marcin\'') # Após a barra ("/"), ignora o próximo caractere 

# r*
print(r"Dj\"Marcin\"")

# *: Melhor não utilizar pois complica o código
